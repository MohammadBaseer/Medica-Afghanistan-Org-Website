<p style="font-size: 13px;">Afghan women and girls have made some significant gains since 2001, notably in areas such as political participation, education and employment. Yet these gains are both deceptive and fragile. In the first place, advances in women’s basic human rights inscribed on paper are ignored in practice; the Constitution of 2004 states that women and men have equal rights before the law, yet Afghanistan’s multiple systems of national, customary and Sharia law set forth contradictory prescriptions for unequal treatment, and the corrupt, dysfunctional justice system is anything but gender blind.</p><br>
                            <p style="font-size: 13px;">Secondly, the gains made by women are unequally distributed; access to education and employment enjoyed by many urban women does not extend to rural areas and more conservative parts of the country. Finally, there is real danger that gains seemingly made in the last ten years will be stymied or reversed. There are simply no durable laws, judicial procedures, or governmental structures to sustain them, and scarcely a single male political or social leader to speak in their favour. The prevailing discrimination against women is evident in the simple facts of social life. Between 2005 and 2010, 66 percent of boys attended primary school, but only 40 percent of girls; and 75 percent of girls drop out in the first few years</p><br>
                            <p style="font-size: 13px;">High school is attended by 18 percent of boys, but only 6 percent of girls. More than 40 percent of men are literate, but only 12 percent of women. Men may have four wives, women only one husband. Divorce is easy for men and extremely difficult for women. A divorced woman cannot live alone but must return to her family, which may refuse to take her in. Moreover, customary marital practices are harmful and dangerous for women. Although forced marriage and child marriage are illegal, 57% of Afghan girls are married before the legal age of 16 (18 years for boys), and 60-80% of marriages are forced. Early marriage compels girls to drop out of school, and early childbearing threatens their health and their lives.</p><br>
                            <p style="font-size: 13px;">The maternal death rate in 2010 placed Afghanistan only slightly ahead of several impoverished African nations, while the infant death rate remains the highest in the world. In Afghanistan women have suffered oppression for generations under the weight of cultural traditions that value them simply as the property of men. More recently, politicized interpretations of Islam have been used to silence them. The years of Taliban rule systematized the oppression and confinement of women, leaving a lasting chill on the cultural climate. Add to these historic trials more than thirty years of war that devastated the countryside and destroyed the major cities.</p><br>
                            <p style="font-size: 13px;">In such times of conflict and insecurity, women and girls are particularly vulnerable to military and political violence. What’s more, warfare that lays waste a country also damages social values, family structures, and the very qualities of mind and spirit that make us human. Many Afghans have lost not only their families and homes but also their sense of compassion, empathy and common humanity. When the powerful, be they rulers or family members, wreak violence upon the weak, women and girls become targets for special kinds of violence.</p><br>
                            <p style="font-size: 13px;">These include rape, physical assault and battering, abduction, forced marriage, child marriage, physical confinement, forced prostitution, starvation, torture, mutilation, murder, so-called honour killings, and political assassination. Such violence against women and girls in Afghanistan is endemic. A nationwide survey of 4,700 women, published in 2008, found that 87.2% had experienced at least one form of physical, sexual, or psychological violence or forced marriage in their lifetime. Research carried out by the Afghan Independent Human Rights Commission (AIHRC) indicates that more than 50 percent of Afghan women are targets of domestic violence. Such violence drives women to run away from their homes, risking capture and imprisonment or worse, and to take their own lives.</p><br>
                            <p style="font-size: 13px;">A study conducted by Medica Afghanistan in 2006 in Kabul, Wardak and Herat provinces showed that 40 percent of women and girls who self-immolated—burned themselves to death—had faced violence by their husbands or in-laws, 29 percent had been forced into marriage, and 30 percent were child wives. Afghan women have lived and still live in a volatile, patriarchal society where brutality and violence are used as tools of oppression.</p><br>
                            <p style="font-size: 13px;">The experience of Medica Afghanistan in assisting oppressed women leads to the conclusion that violence is a kind of contagion, a poisonous and toxic behaviour transmitted not only from one perpetrator to another but also from the perpetrator to his victim who may inflict the violence done to her upon others. In particular, women may turn against women and children—one wife against another, a mother-in-law against her son’s bride, a mother against her own child—each one a victim of violence victimizing another. Women who have been raped or forced into marriage, for example, may become emotionally numb and lose the sense of empathy.</p><br>
                            <p style="font-size: 13px;">In place of human sympathy come jealousy and envy between family members and generations, and anger that may be acted out again in violence. Today women account for approximately 49 per cent of Afghanistan’s estimated 30 million people. Their current situation presents a serious challenge to human development. Afghan women remain among the worst off in the world, first in comparison to their male counterparts, and second in comparison to women of other countries. Their situation in relation to health care, education, and protection from violence is particularly appalling. In trying to assist them, Medica Afghanistan encounters great obstacles. Growing insecurity in the country overrun with opposing Afghan and foreign forces bring widespread fear, distrust and withdrawal. It sentences women to silence, confinement and isolation.</p><br>
                            <p style="font-size: 13px;">In addition, conservative religious attitudes toward women increasingly emerge in politics—in the denunciation of women’s shelters, for example—and are enacted in unconstitutional discriminatory laws. Thus, it becomes more and more difficult for women to have the confidence to claim their rights, and for women’s rights defenders such as Medica Afghanistan to speak out. Finally, the judiciary itself, which should uphold the law and the constitution that guarantees the rights of women, instead—whether by ignorance or corruption—fails to respect them.</p><br>
                            <p style="font-size: 13px;">At present, then, the contribution of Afghan women to their families, to the country’s economy as workers, and to the country’s future as positive agents of change goes unrecognised, while the hard won gains that women have made in the past decade hang in the balance. In this context, Medica Afghanistan continues to stand up for women.</p><br>




