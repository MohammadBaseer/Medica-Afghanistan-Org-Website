<h4 class="ptitle"><strong>Psychosocial and Health Program (PSHP)</strong></h2>
                        <img src="../assets/images/pic4.jpg" alt="" class="img-responsive">
<br>
<p style="font-size: 13px;">Three decades of war have profoundly affected the mental health of women and their families. Several studies have found high levels of depression and other psychosocial problems evidenced by Afghan people, particularly women, as a result of the violence. Yet to talk about such problems is taboo in Afghan society, and very few professionals are qualified to offer psychosocial counseling. Most Afghan women still have little or no access to basic health care services, much less to the limited psychosocial services available only in major cities.</p><br>

<p style="font-size: 13px;">Against this background, Medica Afghanistan’s staff of psychologists, highly trained by international specialists in trauma counseling, initiated services for traumatized women and girls in Afghanistan in 2002. Today, Medica Afghanistan is one of very few agencies in Afghanistan offering both services and training.</p><br>

<p style="font-size: 13px;">Among its direct services, Medica Afghanistan’s PSHP program offers supportive psychosocial counseling in individual and group sessions at key locations throughout Kabul, such as women’s shelters and prisons, the Juvenile Center and Bagh-e-Zanana (Women’s Garden), and other sites in Districts five, seven, eleven and thirteen.</p><br>

<p style="font-size: 13px;">Medica Afghanistan also maintains counseling centers in Herat, at the main hospital, and in Kabul at Rabia Balkhi and Stomatology hospitals where psychologists work jointly with Medica Afghanistan-trained medical staff to provide support to female patients who suffer psychological and physical damage as a consequence of traumatic experiences such as battering, rape, mutilation, self-immolation, or adolescent maternity.</p><br>

<p style="font-size: 13px;">Medica Afghanistan’s psychologists also provide short term counseling to bed-ridden patients during their hospitalization, especially those in the major surgical units and the burn units in Kabul and Herat, and they offer crisis intervention for clients and medical professionals if needed. For women and girls who participate in group counseling and others, Medica Afghanistan’s PSHP program also provides literacy classes to help them increase their reading and writing skills and their awareness of the legal rights of women and girls.</p><br>

<p style="font-size: 13px;">In addition, Medica Afghanistan provides trainings, sensitization, capacity building, and supervision. It offers certified courses to medical professionals and psychologists working with traumatized women and girls.</p><br>

<p style="font-size: 13px;">The training focuses on trauma work in a medical setting: using a trauma-sensitive approach, developing counseling skills, and mastering multiple issues related to violence against women and its consequences for women, their families, and society. Medica Afghanistan’s PSHP team also networks with multiple stakeholders including governmental, national, and international civil society organisations as it provides professional trainings in the provinces, case supervision and monitoring, and referrals for women and girls in need.</p>
