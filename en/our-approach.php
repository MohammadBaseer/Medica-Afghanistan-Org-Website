<p style="font-size: 13px;">Afghan women and girls were deprived of their social and family rights during the devastating civil war and the Taliban Regime. Medica Afghanistan took the initiative soon after the establishment of the government of the Islamic Republic to lend a hand to Afghan women and girls who had survived personal violence and war. Today the advancement of women’s rights and the protection and recovery of women from sexualized and other forms of gender-based violence are at the core of Medica Afghanistan’s work. <br>
Medica Afghanistan empowers Afghan women by directly providing psychosocial services and legal aid. In addition, Medica Afghanistan works on sensitizing men to the key issues of gender-based violence. The organisation offers advanced professional training to lawyers, doctors, social workers and religious leaders on multiple aspects of violence against women. It also works to raise public awareness on women’s rights, and lobbies for local and national policies that aim to end violence against women and girls.</p><br>
                            <p style="font-size: 13px;"><strong>
The approach of Medica Afghanistan is unique in important aspect:</strong></p>
                            <p style="font-size: 13px;">Medica Afghanistan is a women’s organisation. All of our psychosocial counselors, lawyers, social workers, and advocacy staff are female. This is most important to our target group, women and girls, who are reluctant to speak to male counselors, lawyers or social workers about their problems, especially when it comes to sexual violence.
Medica Afghanistan is specialized in supporting women and girls who are deeply affected by violence; all our programs and activities are focused upon them. All our staff members are aware of the relation between gender and violence, and they have extensive training in working with traumatized women and girls. In addition, they all have profound knowledge and a strong commitment to women’s rights.
Medica Afghanistan takes a holistic approach and provides a range of different services for women and girls, including psychosocial counseling, legal representation, social support, literacy classes, and referrals for health care, economic opportunities, and other appropriate services as needed. In addition we advocate publicly and lobby politically for the enhancement of women’s rights to bring long-term, sustainable change for women and girls within a peaceful Afghanistan.</p><br> 

                            <p style="font-size: 13px;"><strong>We are specialized in law, gender, mental health and trauma:</strong></p>
                            <p style="font-size: 13px;">We specialize in a mixed of disciplines: law, gender, mental health and trauma. We are also trained in civil law, criminal law, international human rights law and Shariah law. As such, we are able to deliver specialised services which are holistic and which cater to the multi-faceted needs of survivors of violence.</p><br>
                            <p style="font-size: 13px;"><strong>We are approachable to women clients:</strong></p>
                            <p style="font-size: 13px;">Medica Afghanistan is founded and led by women for women. All of our psychosocial counsellors, lawyers, social workers, and advocacy staff are women. Our clients are comfortable to open up to us about some of their most personal issues. Due to the trust that our clients have in us, they open up to us, allowing us to defend their full interest in court.</p><br>
                            <p style="font-size: 13px;"><strong>We provide holistic support:</strong></p>
                            <p style="font-size: 13px;">Survivors of violence have complex legal, health and support needs. As such, we take a gender and trauma-sensitive approach to our legal, health and support services. We also specialize in handling cases of sexual violence which is particularly sensitive in Afghanistan and which requires legal, health and community responses. We understand that justice must be holistic and survivor-centered. We empower our clients to claim their rights in courts and support them through trial through a specialised psychosocial team. We help them manage their symptoms and build resilience so that they can heal, recover and move on with their lives.</p><br>
                            <p style="font-size: 13px;"><strong>We work closely with communities:</strong></p>
                            <p style="font-size: 13px;">We train and empower former clients to facilitate Peer Support Groups in the community and at the district levels. We connect our clients to this support network so that they can be supported long after the case is closed. They are supported with literacy classes, life skills, basic legal rights awareness and reproductive health education, which they in turn deliver to other women in their communities. By working with a community-based approach, we not only work on the empowerment of individual survivors of violence, but contribute to healthy relationships and social change within the communities.</p><br>
                            <p style="font-size: 13px;"><strong>We work closely with government authorities:</strong></p>
                            <p style="font-size: 13px;">We work with several ministries to push for systemic change in the justice and health system. This has led to new government initiatives to deliver services targeted to women’s multi-faceted needs such as specialised hospital units for self-burn victims, mental health counselling, specialised prosecution units and the recently constituted EVAW court.</p><br>
                            <p style="font-size: 13px;"><strong>Our advocacy is based on and supported by evidence:</strong></p>
                            <p style="font-size: 13px;">We are one of a few organizations in Afghanistan that systematically documents each and every case. Every year, we report on our cases and ensure that the experiences of our clients and their needs are channeled into our advocacy work. All our actions are research and evidence-based. This has enabled us to give meaningful inputs to laws, policies and strategies.</p><br>




