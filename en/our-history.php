<h4>
	<strong>MEDICA AFGHANISTAN – WOMEN SUPPORT ORGANISATION</strong>
</h4>

<p style="font-size: 13px;">Violence against women in Afghanistan has historically been a pervasive but silent issue. We believe that in order to eliminate violence against women, we must empower them to assert their rights and help them heal from their injuries and have the will to move on. This is at the core of our work.</p><br>
                            <p style="font-size: 13px;">Medica Afghanistan (MA) is a non-profit, non-political, non-governmental women’s organization. We support women and girl survivors of gender-based violence and women and girls in conflict with the law, through robust legal representation and defence, psychosocial counseling, and, advocacy.</p><br>
                            <p style="font-size: 13px;">MA was established in Kabul in 2002 by Medica Mondiale, a German-based non-governmental organisation defending women and girls in war and crisis zones throughout the world. With the objective of building a movement led by Afghan women for Afghan women, in December 2010, MA was officially registered with the Ministry of Economy of Afghanistan as a national organisation.</p><br>
                            <p style="font-size: 13px;">Headquartered in Kabul, we operate in Kabul and the north and west regions of Afghanistan through our offices in Balkh, Herat, Samangan and Baghlan. Our services are available to women in neighboring provinces, reaching women in at least 12 provinces of Afghanistan.</p><br>
                            <p style="font-size: 13px;">Since 2002, MA has supported and empowered <strong>16,834 survivors of violence</strong> through legal aid, mediation, awareness raising, training and advocacy. We have also counseled approximately <strong>7,350 women and girls</strong> and established peer-to-peer support networks within communities. Our clients are amongst the most vulnerable in their community. Of the tens and thousands of women we have helped, they have included detainees, <strong>the disabled, minority groups, juveniles, minors, families of women who have been murdered in cold blood and victims of sexual exploitation and human trafficking</strong>.</p><br>
                            <br><h4><strong>Medica Afghanistan runs the following programs:</strong></h4><br>
                            <h4><strong>1.      PSYCHOSOCIAL AND HEALTH PROGRAM (PSHP)</strong></h4><br>
                            <ol>
                                <li><p style="font-size: 13px;"><strong>
                                Counselling at the bed and emergency and crisis cases:</strong> PSHP also maintains consultation rooms in Kabul’s Rabia-e-Balkhi, Malalai, and Cure hospitals and in Herat regional hospital and plans to establish a similar service in Mazar-e-Sharif, and Samangan. MA offers counselling services at the bed for clients who cannot come to the counselling center due to a disability, sickness or being a candidate for surgery and who are in need of emergency counselling. When the client has passed a severe crisis event and she needs psychosocial counselling, MA offers psychosocial counselling at any place that the client is present. It also offers crisis intervention for clients and medical professionals if needed.</p>
                                </li>
                                <li><p style="font-size: 13px;"><strong>
                                    Counselling at the bed and emergency and crisis cases:</strong> PSHP also maintains consultation rooms in Kabul’s Rabia-e-Balkhi, Malalai, and Cure hospitals and in Herat regional hospital and plans to establish a similar service in Mazar-e-Sharif, and Samangan. MA offers counselling services at the bed for clients who cannot come to the counselling center due to a disability, sickness or being a candidate for surgery and who are in need of emergency counselling. When the client has passed a severe crisis event and she needs psychosocial counselling, MA offers psychosocial counselling at any place that the client is present. It also offers crisis intervention for clients and medical professionals if needed.</p>
                                </li>
                                <li><p style="font-size: 13px;"><strong>
                                    Self-Help / Peer-to-Peer Support Groups:</strong> We link clients with other women and former clients so that they can share their problems, break isolation and learn coping mechanisms.</p>
                                </li>
                                <li>
                                    <p style="font-size: 13px;"><strong>Literacy classes and information:</strong> regarding women’s rights and women’s reproductive health are provided for women and girls who participate in MA’s self-help groups. MA enables women to practice better communication skills. We help women to know their rights and cope with the stress caused by violence and trauma and exercise their right to take part in decision-making in a constructive manner and thus to improve their lives.</p><br>
                                </li>

                                
                            </ol><br>
                            
                            <h4><strong>2.      LEGAL AID PROGRAM (LAP)</strong></h4><br>
                            <ol>
                                <li><p style="font-size: 13px;"><strong>
                                        Legal Advice and Awareness:</strong> Every morning, our lawyers and social workers visit the Mediation Centre located in the Governor house / Walyat Custody in Kabul and in the department of MOJ in Mazar and Herat provinces to provide legal advice and awareness to anyone who walks in. Clients are referred to psychologists, hospitals and other support as necessary. We commence legal action when instructed.</p>
                                
                            </li>
                            <li><p style="font-size: 13px;"><strong>Legal Representation in civil and criminal cases:</strong> Our lawyers visit prisons and get referrals from police, hospitals and MOWA to identify clients. Defense consists of advising and representing the defendant during all stages of the legal proceedings until she is released. MA’s lawyers also represent clients during civil proceedings and serve the client until her case is settled in court. We write petitions, study the court files, assist in investigations, work out a solid defense statement, and represent clients in court; In case of violence against women, we also help women to press charges against their perpetrators.</p></li>
                            <li><p style="font-size: 13px;"><strong>Mediation:</strong> When violence is less serious or when clients provide their consent for mediation in their cases, we will mediate disputes between client and family at the mediation centre and help them come to a settlement.  In providing mediation services, MA’s lawyers and social workers work together: the lawyer handles legal matters and provides information about Afghan civil law and Sharia, while the social worker negotiates to resolve family issues and inform them carefully about their legal rights.</p></li>
                            <li><p style="font-size: 13px;"><strong>Social Work:</strong> To better answer the needs of women in detention centers, MA is engaged in contacting clients’ families and establishing links, conveying requests to see their children, arranging family visits, addressing material needs, and laying the groundwork for successful family reintegration after the women are released, or if need be, admission to a women’s shelter. We assess clients’ risks and needs and conduct necessary follow-ups with psychosocial counselling, health care and referrals to employment and training opportunities.</p></li>

                        </ol>
                        <h4><strong>3.      ADVOCACY  </strong></h4><br>
                            <p style="font-size: 13px;">According to its dual strategy and in addition to providing direct services to women, MA works diligently to improve the quality of women’s lives by advocating for their human rights. The purpose of the advocacy work is to bring about positive change in legislation, policies, and public attitudes towards women. We also contribute to bring positive changes in the government structure in regard to women rights and creation of protection culture in the justice system.
</p><br>
<p style="font-size: 13px;">
We regularly lobby the Supreme Court, MOJ, MOPH, President and First Lady’s offices and MOWA for reform of law and policy, as well as issues which arise from our cases. We also monitor enforcement and implementation of the laws. We document cases where the State committed systemic breaches of client’s rights in order to hold the State accountable.
</p><br>
<p style="font-size: 13px;">
We are one of the few organizations in Afghanistan which systematically document each and every case. Every year, we report on our cases and ensure that the experiences of our clients and their needs are channeled into our advocacy work. All our actions are research and evidence-based. This has enabled us to give meaningful inputs to laws, policies and strategies.
</p><br>
<p style="font-size: 13px;">
These efforts are realized by conducting poster and radio campaigns to counteract domestic violence, conferences for the judiciary on domestic violence and women’s rights within the family, events to publicize UN Security Council Resolution 1325 calling for the active participation of women in all phases of peace-making and development, national conferences to push for enforcement of EVAW law etc.
</p><br>
<p style="font-size: 13px;">
Medica Afghanistan also participates actively in the Afghan Women’s Network, ACBAR and various working groups’s advocacy and lobbying events to empower women to take part in the decision-making processes, to work on the amendments of laws and policies and to improve the legal status of women and girls. We do this through various working groups and committees such as: Law Reform Working Group, Gender Technical Working Group, Afghanistan Legal Aid and Advocate Network (ALAAN) Working Groups, CEDAW Initiative Committee and Protection of Women against Forced Gynecological Examinations Working Committee.</p><br>
<h4><strong>4.      CAPACITY BUILDING AND TRAINING</strong></h4><br>
<p style="font-size: 13px;">In addition to delivering direct services to women, MA provides informative training and capacity-building upon request to selected NGOs, national, district, and community governmental bodies, and religious authorities on issues pertaining to women embodied in international human rights agreements to which our government has ratified. We work on sensitizing men and women to the key issues of gender-based violence and its impact on individual and society by providing multiple trainings for health and legal professionals and others, for example, for mullahs, medical staff, prosecutors, police officers or judges on developing their counseling skills, mastering multiple issues related to violence against women and its consequences for women, their families, and society, and finally developing their skills on trauma work in medical and legal settings to adopt a stress and trauma sensitive approach towards women affected by violence. We raise their awareness on women’s rights and persuade them to change their attitude and behavior. </p><br>

