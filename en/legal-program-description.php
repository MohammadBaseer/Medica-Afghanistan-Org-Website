<h4 class="ptitle"><strong>Legal Aid Program (LAP)</strong></h4>
                        <img src="../assets/images/pic7.jpg" alt="" class="img-responsive">

<br>

<p style="font-size: 13px;"> Medica Afghanistan’s Legal Aid Program responds to Afghan women’s need for justice by trying to fill a big gap; legal services for women facing criminal prosecution or trying to engage in civil proceedings are extremely limited. Medica Afghanistan’s legal aid services include criminal defence, representation in civil proceedings, mediation, raising legal awareness, providing legal advice, and facilitating safe family reintegration.</p><br>

<p style="font-size: 13px;">Medica Afghanistan’s professionally trained lawyers undertake the criminal defence of women and girls prosecuted in the cities of Kabul, Mazar-e-Sharif, and Herat free of charge. In most cases, the process starts in a detention center or juvenile rehabilitation facility where Medica Afghanistan’s lawyers go to offer assistance to detained women and girls. Defence consists in advising and representing the defendant during all stages of the legal proceedings until she is released.</p><br>

<p style="font-size: 13px;">Medica Afghanistan’s lawyers also represent clients during civil proceedings, whether initiated by the client or brought against her. Most civil clients seek out Medica Afghanistan’s legal services at the offices they maintain on the grounds of provincial department of justice compounds. The lawyers serve the client until her case is settled in court.</p><br>

<p style="font-size: 13px;">Medica Afghanistan’s mediation work consists in intervening in disputes in order to resolve them and reconcile the parties. Usually the party seeking help is a woman involved in a domestic dispute with her husband or in laws that threatens her rights within the family. More than half the complaints are brought by battered women, many with severe physical injuries.(Lawyers refer many women who seek their help to Medica Afghanistan’s psychosocial counselors because the women show signs of psychological distress and traumatization.)</p><br>

<p style="font-size: 13px;">In providing mediation services, Medica Afghanistan’s lawyers and social workers work together; the lawyer handles legal matters and provides information about Afghan law and Sharia, while the social worker negotiates to resolve family issues. Mediation can also be used to complement a divorce proceeding before the court, enabling the parties to reach an agreement about the divorce itself and related issues such as child custody, alimony, and financial compensation.</p><br>

<p style="font-size: 13px;">While providing legal aid, Medica Afghanistan’s lawyers have a duty to raise the client’s awareness of legal matters. The lawyers carefully inform the client about her legal rights, the proceedings that will take place, the possible procedural alternatives open to her and their legal consequences, the remedies and compensation she is entitled to, and so on.</p><br>

<p style="font-size: 13px;">Raising the client’s awareness is intended to help her make her own decisions about her case. Legal awareness is all the more important to Medica Afghanistan’s legal aid clients because most of them are non-literate and do not know they possess legal rights that they are entitled to claim. Many women visit Medica Afghanistan’s legal aid offices at the provincial departments of justice, having learned about the legal services by word of mouth, while others are referred to the legal aid lawyers by their colleagues on the psychosocial team. Medica Afghanistan offers legal advice to all of them, whether or not they are involved in a criminal or civil proceeding or a mediation process.</p><br>

<p style="font-size: 13px;">In 2009, Medica Afghanistan established social services to better answer the needs of women benefiting from legal aid. An assessment conducted by Medica Afghanistan staff had revealed that most women and girls in detention suffer greatly by being isolated from their families and friends in the outside world. In addition, women and girls released from prison to return to their homes faced many serious problems, from anxiety to rejection by their families and communities.</p><br>

<p style="font-size: 13px;">With the establishment of the social work component, Medica Afghanistan social workers are able to attend to the social needs of women in detention: contacting their families and establishing links, conveying requests to see their children, arranging family visits, addressing material needs, and laying the groundwork for successful family reintegration after the women are released, or if need be, admission to a women’s shelter. When women and girls are freed from detention, social workers also follow-up by referring women to appropriate services, such as medical facilities and organisations providing job training and opportunities to women.</p>




